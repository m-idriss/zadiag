import { readFile, readdir, mkdir, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { validateRoutinePackage } from './routine-package.mjs';

const root = resolve(import.meta.dirname, '..');
const routineDir = resolve(root, 'routines');
const files = (await readdir(routineDir)).filter((file) => file.endsWith('.json') && !['schema.json', 'template.json'].includes(file)).sort();
const fail = (file, message) => { throw new Error(`${file}: ${message}`); };
const routines = [];
validateRoutinePackage('template.json', JSON.parse(await readFile(resolve(routineDir, 'template.json'), 'utf8')));
for (const file of files) {
  const routinePackage = JSON.parse(await readFile(resolve(routineDir, file), 'utf8'));
  const routine = validateRoutinePackage(file, routinePackage);
  if (file !== `${routine.id}.json`) fail(file, 'filename must match the routine id');
  routines.push(routine);
}
const ids = new Set();
routines.forEach((routine) => { if (ids.has(routine.id)) fail(routine.id, 'duplicate routine id'); ids.add(routine.id); });
if (!ids.has('orthodontic-elastics')) throw new Error('The default orthodontic-elastics routine is required.');

const banner = '// Generated by scripts/generate-routine-catalog.mjs. Do not edit.\n';
const source = `${banner}export const generatedRoutines = ${JSON.stringify(routines, null, 2)} as const;\n`;
const targets = [resolve(root, 'src/generated/routineCatalog.ts'), resolve(root, 'functions/src/generated/routineCatalog.ts')];
const check = process.argv.includes('--check');
for (const target of targets) {
  if (check) {
    const existing = await readFile(target, 'utf8').catch(() => '');
    if (existing !== source) throw new Error(`${target} is stale. Run npm run generate:routines.`);
  } else {
    await mkdir(resolve(target, '..'), { recursive: true });
    await writeFile(target, source);
  }
}
console.log(`Validated ${routines.length} routines${check ? ' and generated catalogs' : ''}.`);
